def parse_match_text(text):
    """–ü–∞—Ä—Å–∏—Ç —Ç–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è —Å –º–∞—Ç—á–∞–º–∏ –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—Ç—Ä—É–∫—Ç—É—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ."""
    try:
        # –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ —Å—Ç–∞—Ç–µ–π –≤ —Å–æ–æ–±—â–µ–Ω–∏–∏
        max_matches_pattern = r'(\d+)\s+—Å—Ç–∞—Ç(–µ–π|—å–∏)'  # –ù–∞–ø—Ä–∏–º–µ—Ä, "5 —Å—Ç–∞—Ç–µ–π" –∏–ª–∏ "10 —Å—Ç–∞—Ç–µ–π"
        max_matches = 5  # –ü–æ —É–º–æ–ª—á–∞–Ω–∏—é –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏–µ - 5 –º–∞—Ç—á–µ–π
        
        max_matches_match = re.search(max_matches_pattern, text, re.IGNORECASE)
        if max_matches_match:
            max_matches = int(max_matches_match.group(1))
            logger.info(f"–£—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏–µ –Ω–∞ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç–∞—Ç–µ–π –≤ parse_match_text: {max_matches}")
    
        # –†–∞–∑–¥–µ–ª—è–µ–º –ø–æ –¥–∞—Ç–∞–º
        date_blocks = []
        current_block = {'date': '', 'deadline': '', 'matches': []}
        total_matches = 0  # –°—á–µ—Ç—á–∏–∫ –≤—Å–µ—Ö –º–∞—Ç—á–µ–π
        
        for line in text.split('\n'):
            line = line.strip()
            if not line:
                continue
            
            # –ü–æ–∏—Å–∫ –¥–∞—Ç—ã –∏ –¥–µ–¥–ª–∞–π–Ω–∞
            date_match = re.match(r'–Ω–∞ (\d+ \w+) \(–Ω–µ –ø–æ–∑–¥–Ω–µ–µ (\d+ \w+)\)', line)
            if date_match:
                if current_block['date']:
                    date_blocks.append(current_block)
                current_block = {
                    'date': date_match.group(1),
                    'deadline': date_match.group(2),
                    'matches': []
                }
                continue
            
            # –ï—Å–ª–∏ –¥–æ—Å—Ç–∏–≥–ª–∏ –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–≥–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –º–∞—Ç—á–µ–π, –ø—Ä–µ–∫—Ä–∞—â–∞–µ–º –æ–±—Ä–∞–±–æ—Ç–∫—É
            if total_matches >= max_matches:
                break
                
            # –ü–æ–∏—Å–∫ –º–∞—Ç—á–µ–π
            match_info = re.match(r'(\d+)\. (.+?)(\(.+?\))?$', line)
            if match_info:
                number = match_info.group(1)
                match_text = match_info.group(2).strip()
                
                min_symbols = 1000  # –ü–æ —É–º–æ–ª—á–∞–Ω–∏—é
                symbols_match = re.search(r'\((\d+)\)', line)
                if symbols_match:
                    min_symbols = int(symbols_match.group(1))
                
                # –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ "–í—Å–µ X –º–∞—Ç—á–µ–π"
                all_matches = re.match(r'–í—Å–µ (\d+) –º–∞—Ç—á–µ–π\s+(.+)', match_text)
                if all_matches:
                    count = int(all_matches.group(1))
                    tournament = all_matches.group(2).strip()
                    # –û–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ "–≤—Å–µ—Ö –º–∞—Ç—á–µ–π" –¥–æ—Å—Ç—É–ø–Ω—ã–º –ª–∏–º–∏—Ç–æ–º
                    if total_matches < max_matches:
                        current_block['matches'].append({
                            'number': number,
                            'is_all_matches': True,
                            'count': min(count, max_matches - total_matches),  # –û–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ–º —Å—á–µ—Ç—á–∏–∫
                            'tournament': tournament,
                            'min_symbols': min_symbols,
                            'date': current_block['date']  # –î–æ–±–∞–≤–ª—è–µ–º –¥–∞—Ç—É –∏–∑ —Ç–µ–∫—É—â–µ–≥–æ –±–ª–æ–∫–∞
                        })
                        total_matches += 1  # –°—á–∏—Ç–∞–µ–º –∫–∞–∫ –æ–¥–∏–Ω –º–∞—Ç—á –≤ –æ–±—â–µ–º —Å—á–µ—Ç—á–∏–∫–µ
                else:
                    # –û–±—ã—á–Ω—ã–π –º–∞—Ç—á
                    teams_tournament = match_text.split('                ')
                    if len(teams_tournament) >= 2:
                        teams = teams_tournament[0].strip()
                        tournament = teams_tournament[1].strip()
                        current_block['matches'].append({
                            'number': number,
                            'is_all_matches': False,
                            'teams': teams,
                            'tournament': tournament,
                            'min_symbols': min_symbols,
                            'date': current_block['date']  # –î–æ–±–∞–≤–ª—è–µ–º –¥–∞—Ç—É –∏–∑ —Ç–µ–∫—É—â–µ–≥–æ –±–ª–æ–∫–∞
                        })
                        total_matches += 1
        
        # –î–æ–±–∞–≤–ª—è–µ–º –ø–æ—Å–ª–µ–¥–Ω–∏–π –±–ª–æ–∫
        if current_block['date']:
            date_blocks.append(current_block)
        
        return date_blocks
    except Exception as e:
        logger.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–∞—Ä—Å–∏–Ω–≥–µ —Ç–µ–∫—Å—Ç–∞: {e}")
        return []

